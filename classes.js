function updateFrame(courseNumber){
    const parsedCourseNumber = parseInt(courseNumber, 10);
    courseNumber = parsedCourseNumber;
    const courseInfo = courses[parsedCourseNumber];

    if (courseInfo) {
        const courseFrame = document.getElementById("courseNumber");
        courseFrame.innerHTML = `<h1 class="course-title">${courseInfo.number}: ${courseInfo.title}</h1>
                              <p class="course-professor"><strong>Professor:</strong> ${courseInfo.professor}</p>
                              <p class="course-description"><strong>Description:</strong> ${courseInfo.description}</p>`;
        const overallRatingBar = document.querySelector('.bar1');
        overallRatingBar.style.setProperty('--overall-rating', courseInfo.rating);
        const difficultyBar = document.querySelector('.bar2');
        difficultyBar.style.setProperty('--difficulty', courseInfo.difficulty);

        const courseLoadBar = document.querySelector('.bar3');
        courseLoadBar.style.setProperty('--course-load', courseInfo.cload);


      } else {
        console.log("Course not found.");
    }
}

function submitForm(event) {
    // prevent the default form submission behavior of refreshing the page
    event.preventDefault();
  
    // get the form values
    const rating = document.getElementById("rating").value;
    const difficulty = document.getElementById("difficulty").value;
    const cload = document.getElementById("cload").value;
  
    // calculate the average rating, difficulty, and cload
    const ratings = Object.values(courseData).map(course => Number(course.rating));
    const difficulties = Object.values(courseData).map(course => Number(course.difficulty));
    const cloads = Object.values(courseData).map(course => Number(course.cload));
    const avgRating = (ratings.reduce((a, b) => a + b, 0) + Number(rating)) / (ratings.length + 1);
    const avgDifficulty = (difficulties.reduce((a, b) => a + b, 0) + Number(difficulty)) / (difficulties.length + 1);
    const avgCload = (cloads.reduce((a, b) => a + b, 0) + Number(cload)) / (cloads.length + 1);
  
    // update the graph
    const bars = document.querySelectorAll(".bar");
    bars[0].style.width = `${avgRating * 20}%`;
    bars[1].style.width = `${avgDifficulty * 20}%`;
    bars[2].style.width = `${avgCload * 20}%`;
  
    // update the course data
    const courseNumber = document.getElementById("course-number").textContent;
    courseData[courseNumber].rating = rating;
    courseData[courseNumber].difficulty = difficulty;
    courseData[courseNumber].cload = cload;
    localStorage.setItem("courseData", JSON.stringify(courseData));
  
    // reset the form
    event.target.reset();
}